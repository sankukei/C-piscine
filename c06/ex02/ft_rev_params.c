#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	y;

	i = ac - 1;
	y = 0;
	if (ac == 1)
		return (0);
	while (i > 0)
	{
		while (av[i] && av[i][y])
		{
			write(1, &av[i][y], 1);
			y++;
		}
		y = 0;
		i--;
		write(1, "\n", 1);
	}
	return (0);
}
